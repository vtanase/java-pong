package com.vlad.pong.geometry;

public class Size {

    private int width = 0;
    private int height = 0;

    public Size(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return this.width;
    }

    public int getHeight() {
      return this.height;
    }
}